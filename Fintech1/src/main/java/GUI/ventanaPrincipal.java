    private void jButtonIngresarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIngresarClienteActionPerformed
        String cedula = this.jTextFieldCedulaCliente.getText();
        usuario existeCliente = this.controlador1.buscarCliente( cedula);
        cuenta existeCuenta = this.controlador1.buscarCuenta(cedula);
        //ventana emergente 
        if ( existeCliente != null ){
           
            JOptionPane.showMessageDialog(this, "cliente encontrado" );
            this.jPanelLoginCliente.setVisible(false);
            this.jPanelCliente.setVisible(true);
            
            this.jLabelNombreCliente.setText(existeCliente.getNombre());
            this.jLabelCuentaCliente.setText(existeCuenta.getNumeroCuenta());
            this.jLabelSaldoCliente.setText(existeCuenta.getSaldo()+"");
            
           
        }else {
            JOptionPane.showMessageDialog(this,"cliente no encontrado " );
        }



    
    }//GEN-LAST:event_jButtonIngresarClienteActionPerformed

